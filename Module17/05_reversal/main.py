print('Введите строку, в которой буква h встречается как минимум два раза.')
txt = input('Строка :')
txt_index = [i for i in range(len(txt)) if txt[i] == 'h']
print('\nМежду первым и последним появлением буквы h '
      'содержится \nследующий текст, развернутый в обратном порядке: ',
      txt[txt_index[len(txt_index) - 1] - 1:txt_index[0]:-1])
